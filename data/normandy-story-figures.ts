export const NORMANDY_STORY_FIGURES = mergeNormandyFigurePortraits(NORMANDY_STORY_FIGURES_RAW);

export type NormandyFigureTimelineFilter = 'all' | 'map' | 'atlas' | 'primary';

export function filterNormandyStoryFigures(options: {
  timeline?: NormandyFigureTimelineFilter;
  eraId?: string;
}): NormandyStoryFigure[] {
  let list = NORMANDY_STORY_FIGURES;
  const { timeline = 'all', eraId } = options;
  if (timeline === 'map') list = list.filter((f) => f.eraSource === 'map');
  else if (timeline === 'atlas') list = list.filter((f) => f.eraSource === 'atlas');
  else if (timeline === 'primary') list = list.filter((f) => f.editorialPriority === 'primary');
  if (eraId) list = list.filter((f) => f.eraId === eraId);
  return list;
}
